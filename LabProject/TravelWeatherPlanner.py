distance_mi = 7
is_raining = False
has_bike = True
has_car = False
has_ride_share_app = True

if distance_mi == 0:
    print(False)
elif distance_mi <= 1:
    if is_raining == 0:
        print(True)
    else:
        print(False)
elif distance_mi >= 1 and distance_mi <= 6:
    if has_bike and is_raining == 0:
        print(True)
    else:
        print(False)
else:
    if has_car or has_ride_share_app:
        print(True)
    else:
        print(False)